#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (dest < src)
	{
		i = 0;
		while (i < n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			++i;
		}
	}
	else if (dest > src)
	{
		i = n;
		while (i > 0)
		{
			--i;
			((char *)dest)[i] = ((char *)src)[i];
		}
	}
	return (dest);
}
