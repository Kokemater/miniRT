#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	if (!s)
		return (0);
	i = 0;
	while (s[i])
	{
		if ((char)c == s[i])
			return ((char *)(s + i));
		++i;
	}
	if ((char)c == s[i])
		return ((char *)(s + i));
	return (0);
}
